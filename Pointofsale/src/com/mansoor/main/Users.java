    private void cbupdate() {
        try {
            dto.Staff_Type[] stafftypes= dto.Staff_TypeDAO.listStaff_TypeByQuery(null, null);
            cb_type.removeAllItems();
            for(int i=0;i<stafftypes.length;i++){
                cb_type.addItem(stafftypes[i].getST_Type());
            }
        } catch (PersistentException ex) {
            Logger.getLogger(Users.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void updateTable() {
        DefaultTableModel tm = (DefaultTableModel) tbl_users.getModel();
        tm.setRowCount(0);
        dto.Staff[] s;
        try {
            Object[] temp = new Object[5];
            s = dto.StaffDAO.listStaffByQuery(null, null);
            for(int i=0;i<s.length;i++){
                temp[0]=s[i].getS_Id();
                temp[1]=s[i].getS_Name();
                temp[2]=s[i].getS_UserName();
                temp[3]=s[i].getS_Password();
                temp[4]=s[i].getST().getST_Type();
                
                tm.addRow(temp);
            }
        } catch (PersistentException ex) {
            Logger.getLogger(Users.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

