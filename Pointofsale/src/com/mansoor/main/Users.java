    private void btn_deleteuserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteuserActionPerformed
        int index= tbl_users.getSelectedRow();      
        if(index==-1){
            JOptionPane.showMessageDialog(this, "Please select user!");
        }
        else{
            int id = (int) tbl_users.getModel().getValueAt(index, 0);
            try {
              int v =  JOptionPane.showConfirmDialog(this, "Do you really want to Delete user?");
                if(v==0){
                    if(id==staffid){
                        int r=JOptionPane.showConfirmDialog(this, "It is your account you will be log out after deleting, Do you want to continue?");
                        if (r==0){
                            dto.Staff s = dto.StaffDAO.getStaffByORMID(id);
                            dto.StaffDAO.deleteAndDissociate(s);
                            login m = new login();
                            m.setVisible(true);
                            close();
                        }else{
                            return;
                        } 
                    }
                dto.Staff s = dto.StaffDAO.getStaffByORMID(id);
                dto.StaffDAO.deleteAndDissociate(s);
                updateTable();
                }
            } catch (PersistentException ex) {
                Logger.getLogger(Users.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_btn_deleteuserActionPerformed

